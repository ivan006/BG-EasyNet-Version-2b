<h4>Accommodation</h4>
<p>Accommodation is a room, a group of rooms, or a building in which someone may live or stay.</p>
<ul>
<li>Destination - click on the dropdown and select the area in which the accommodation is situated.<small>E.g. Botswana.</small></li>
<li>Supplier - type in the name of the accommodation. <small>E.g. Holiday Inn Express.</small></li>
<li>Address - will automatically be inserted by the system. </li>
<li>Room type - the room type will automatically be inserted once you have chosen the accommodation.</li>
<li>Check in & out - select the check out and check in dates from the calendar.</li>
</ul>
</div>
</div>

<div class="row">
<div class="col-md-12">
<h4>Excursion</h4>
<p>An excursion is a short journey or trip, especially one taken as a leisure activity.</p>
<ul>
<li>Destination - click on the dropdown and select the area in which the excursion is situated. <small>E.g. Cape Town CBD</small></li>
<li>Supplier - type in the name of the excursion.<small>E.g. City Sightseeing Cape Town.</small></li>
<li>Address - will automatically be inserted by the system.</li>
<li>Excursion type - will automatically be inserted by the system.</li>
<li>Pick up & Drop off - select the check out and check in dates from the calendar. At the bottom of the calendar you will find the dropdown to select the time for the pick up and drop off.</li>
</ul>

<h4>Meet and Greet</h4>
<p>A Meet & Greet, is usually in the arrivals hall at the airport, where the driver meets the traveller(s) and assist them with their luggage and answers any questions they may have, then the driver will transfer them to their accommodation.</p>

<ul>
<li>Destination - click on the dropdown and select the area in which the driver has to collect the traveller.<small>E.g. Gauteng Pretoria.</small></li>
<li>Supplier - type in the name of the company that will be meeting the traveller at the location.<small>E.g. African Ample Assistance.</small></li>
<li>Physical address - will automatically be inserted by the system.</li>
<li>Service description - will automatically be inserted by the system.</li>
<li>Pick up time - select the pick up date from the calendar. At the bottom of the calendar you will find the dropdown to select the time for the pick up.</li>
</ul>

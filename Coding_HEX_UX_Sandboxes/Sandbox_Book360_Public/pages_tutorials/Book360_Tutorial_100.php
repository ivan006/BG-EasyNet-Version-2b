<h4>How to Access the Tripbuilder:</h4>

<ul>

<li>Click on Tripbuilder Bookings and then select My Bookings</li>
<li>Click on "Create a new booking" and select the "Create a new booking from scratch"</li>
<li>Start the booking process by clicking on <b>Dates</b> and select the amount of days for the trip</li>
<li>Click on the <b>Edit trip information</b> button, it is located at the top right corner of the landing page.<br> Here you must insert the Party name, Agent (click on the drop-down and select the agent) Budget and the Status  (click on the drop-down and select the status) of the booking on this window. To save the information you have filled in click on “Save”.</li>
<li>Click on "<b>Pax Configuration</b>" button, it is located at the top right corner of the landing page and then the “Add room“ button. Here you must insert the Name of the room, Room type (click on the drop-down and select the room type) and add the amount of adults, children and infants (click on “Add adult”, “Add child” and “Add infant”). To save the information you have filled in click on “Save room details”.</li>
<li>After you have selected all the necessary services click on “ Save Booking”.</li>
<li>Once you have saved the booking it will generate the “ABFI” reference number and you will  have the option to either view the booking summary or continue editing in the TripBuilder.</li>
</ul>

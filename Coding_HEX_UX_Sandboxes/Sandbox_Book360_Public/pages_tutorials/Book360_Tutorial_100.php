<h4>Car Rental</h4>
<p>A car rental is a company that rents out vehicles for a short period of time for a certain fee depending on the category and class of the vehicle, as well as the amount of days the vehicle will be out for rental. There are also multiple insurances that can be taken out while the vehicle is in the possession of the customer. </p>
<ul>
<li>Supplier - type in the name of the company you would like to rent a car from. <small>E.g. Bidvest.</small></li>
<li>Pick up location - the place where the traveller has to pick up the car. <small>E.g. BedFordView Eastgate.</small></li>
<li>Drop off location - the place where the traveller has to drop off the car.<small>E.g. Claremont, Cavendish Square.</small></li>
<li>Service description - will automatically be inserted by the system.</li>
<li>Pick up time - select the pick up date from the calendar. At the bottom of the calendar you will find the dropdown to select the pick up time.</li>
<li>Drop off time - select the drop off date from the calendar. At the bottom of the calendar you will find the dropdown to select the drop off time.</li>
</ul>
